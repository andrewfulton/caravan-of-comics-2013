    <h2>Gregory Mackay</h2>
    <div id="artist-content-text-inner">
        <p>Gregory Mackay is best known for his comic strip <em>Francis Bear</em> as well as award-winning autobiographical comics, he makes award-winning comics about a strange kind of every-day-ness that are both quietly desperate and charmingly beautiful. Gregory is a regularly featured artist in the <em>French Turkey Comix</em> anthology published by The Hoochie Coochie, who in 2010 released a collected <em>Francis Bear</em> book.</p>
