    <h2>Mirranda Burton</h2>
    <div id="artist-content-text-inner">
        <p>Since graduating from art school in South Australia, Mirranda Burton has forged pathways in independent and commercial animation, drawing, and printmaking. More recently she has devoted herself to the medium of comics, drawing upon her own life experiences. Her stories depict a myriad of moments, from the mountain peaks of Morocco to lettuce invasions, to rare meetings with caterpillar psychiatrists and vacuum cleaner genies. Her first book, <em>Hidden</em>, was published in 2011 by Black Pepper, for which she received an Aurealis Award for best graphic novel. <em>Hidden</em> is also scheduled to be published in France.</p>
