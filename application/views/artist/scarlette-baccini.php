        <p>Scarlette Baccini is a comic book writer and illustrator from Melbourne, Australia. In 2007 she began writing a strip called <em>Zombolette</em>, about a fat, obnoxious zombie and her giant mutant guinea pig housemate. Milk Shadow Books published a 120-page anthology of this work early in 2012. Scarlette is currently working on a graphic novel as well as several shorter works, and is hoping to turn <em>Zombolette</em> into an animated series.</p>
