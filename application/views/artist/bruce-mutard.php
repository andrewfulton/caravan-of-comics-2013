    <h2>Bruce Mutard</h2>
    <div id="artist-content-text-inner">
        <p>Melbourne based, self described 'writer with images', has had 5 books published, including the highly regarded graphic novels <em>The Sacrifice</em> and <em>The Silence</em> (Allen & Unwin), <em>A Mind of Love</em> (Black House Comics) and the short story collection, <em>Stripshow</em> (Milkshadow Books). He has had numerous short stories published in <em>Overland</em>, <em>Meanjin</em>, <em>The Australian Book Review</em> and the <em>Tango</em> anthology among many others. He is also the holder of the Australian Society of Authors comics and graphic novels portfolio. He has presented papers at Oxford, the University of Arts, London, Loughborough University, teaches, organises comics events and tours like this one, and was recently awarded an Australia Council of Arts established writers grant to work on his next graphic novel. </p>
